package sales.pipesandconduit.tracker;

public class Category {

    public String key;
    public String name;

    public Category() {
    }

    public Category(String key, String name) {
        this.key = key;
        this.name = name;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
